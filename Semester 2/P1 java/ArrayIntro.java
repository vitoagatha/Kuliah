public class ArrayIntro{
    public static void main(String[] args) {
        int usiaAni = 16;

        int[] usia = {10, 11, 12};
        String[] namaOrtu = {"Jamal", "Eko", "Budi"};

        // Menggani nilai manual
        usia[1] = 15;

        System.out.println("Usia Ani " + usiaAni);
        System.out.println("Usia " + usia[0]);
        System.out.println("Usia " + usia[1]);
        System.out.println("Usia " + usia[2]);

        System.out.println("Nama Orang tua " + namaOrtu[0]);
        System.out.println("Nama Orang tua " + namaOrtu[1]);
        
    }
}