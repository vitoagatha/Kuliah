public class CompareTo {
    public static void main(String[] args){

        System.out.print("Comparing \"axe\" and \"dog\" produces ");
        int a = "axe".compareTo("dog");
        System.out.println(a);

        System.out.print("Comparing \"halo\" and \"halo\" produces ");
        int b = "halo".compareTo("halo"); 
        System.out.println(b);

        System.out.print("Comparing \"pesawat\" and \"apel\" produces ");
        int c = "pesawat".compareTo("apel");
        System.out.println(c);

    }
}
